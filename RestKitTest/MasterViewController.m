    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell" forIndexPath:indexPath];
    Train *train = self.departures[indexPath.row];
    cell.textLabel.text = train.destination;
    NSLog(@"%@", train.destination);
    return cell;
}

